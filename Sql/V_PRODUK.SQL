
CREATE
    VIEW `pusb8564_dataku`.`V_PRODUK` 
    AS
(SELECT A.TYPE, A.CODE, A.NAME, A.DESCRIPTION, A.STATUS 
FROM MS_PRODUK_PRABAYAR AS A
UNION
SELECT B.TYPE, B.CODE, B.DESCRIPTION AS NAME, B.DESCRIPTION, B.STATUS 
FROM MS_PRODUK_PASCABAYAR AS B
UNION
SELECT C.TYPE, C.CODE, C.NAME, C.DESCRIPTION, C.STATUS 
FROM MS_PRODUK_EMONEY AS C
UNION
SELECT D.TYPE, D.CODE, D.NAME, D.DESCRIPTION, D.STATUS 
FROM MS_PRODUK_GAME AS D);
